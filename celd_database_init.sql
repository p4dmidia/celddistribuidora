-- C. Trigger: Configurar Dinamicamente a Organização (Tenant) no Cadastro
CREATE OR REPLACE FUNCTION public.handle_auth_user_app_metadata()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
AS $function$
DECLARE 
    v_org_id uuid;
BEGIN
  -- Tenta resgatar organization_id do cadastro do usuário
  v_org_id := NULLIF(new.raw_user_meta_data ->> 'organization_id', '')::uuid;
  
  -- Se estiver vazio, joga no ID padrão da CELD Distribuidora
  IF v_org_id IS NULL THEN
     SELECT id INTO v_org_id FROM public.organizations WHERE name = 'CELD Distribuidora' LIMIT 1;
  END IF;

  -- Insere no app_metadata para validação segura via JWT nas políticas RLS
  new.raw_app_meta_data := coalesce(new.raw_app_meta_data, '{}'::jsonb) || jsonb_build_object('organization_id', v_org_id);
  
  RETURN new;
END;
$function$;

DROP TRIGGER IF EXISTS on_auth_user_app_metadata ON auth.users;
CREATE TRIGGER on_auth_user_app_metadata
  BEFORE INSERT OR UPDATE ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_auth_user_app_metadata();


-- D. Trigger: Sincronização e Cadastro Automático do Afiliado (Após Inserção no Auth.Users)
CREATE OR REPLACE FUNCTION public.handle_new_affiliate_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
 AS $function$
 DECLARE
   v_full_name text;
   v_sponsor_affiliate_id uuid;
   v_sponsor_user_id uuid;
   v_org_id uuid;
   v_sponsor_code text;
   v_login text;
   v_temp_login text;
   v_exists boolean;
   v_counter integer := 0;
 BEGIN
   -- 1. Determinar Organização de forma DINÂMICA
   v_org_id := NULLIF(new.raw_user_meta_data ->> 'organization_id', '')::uuid;
   
   IF v_org_id IS NULL AND (new.raw_user_meta_data ->> 'organization_name') IS NOT NULL THEN
      SELECT id INTO v_org_id FROM public.organizations 
      WHERE LOWER(name) = LOWER(new.raw_user_meta_data ->> 'organization_name') 
      LIMIT 1;
   END IF;

   -- Fallback de segurança para CELD Distribuidora
   IF v_org_id IS NULL THEN
      SELECT id INTO v_org_id FROM public.organizations WHERE name = 'CELD Distribuidora' LIMIT 1;
   END IF;

   -- Se ainda não existir a organização no banco, lança erro
   IF v_org_id IS NULL THEN
      RAISE EXCEPTION 'Multi-tenant Error: Nenhuma organização padrão encontrada.';
   END IF;

   -- 2. Determinar Dados Básicos do Usuário
   v_login := LOWER(TRIM(COALESCE(new.raw_user_meta_data ->> 'login', split_part(new.email, '@', 1))));
   v_full_name := TRIM(CONCAT_WS(' ', new.raw_user_meta_data ->> 'nome', new.raw_user_meta_data ->> 'sobrenome'));
   IF v_full_name = '' OR v_full_name IS NULL THEN 
     v_full_name := COALESCE(new.raw_user_meta_data ->> 'nome', v_login); 
   END IF;

   -- 3. Evitar colisão de código de indicação (referral_code)
   v_temp_login := v_login;
   LOOP
     SELECT EXISTS (
       SELECT 1 FROM public.affiliates 
       WHERE LOWER(referral_code) = v_temp_login 
       AND organization_id = v_org_id
     ) INTO v_exists;
     
     EXIT WHEN NOT v_exists OR v_counter > 10;
     
     v_counter := v_counter + 1;
     v_temp_login := v_login || substr(md5(random()::text), 1, 4);
   END LOOP;
   v_login := v_temp_login;

   -- 4. Resolver Padrinho (Sponsor) dentro do mesmo inquilino
   v_sponsor_code := NULLIF(new.raw_user_meta_data ->> 'sponsor_code', '');
   
   IF v_sponsor_code IS NOT NULL THEN
     SELECT id, user_id INTO v_sponsor_affiliate_id, v_sponsor_user_id
     FROM public.affiliates 
     WHERE LOWER(referral_code) = LOWER(v_sponsor_code)
     AND organization_id = v_org_id
     LIMIT 1;
   END IF;

   -- Fallback para o afiliado raiz da CELD se nenhum patrocinador for informado
   IF v_sponsor_user_id IS NULL THEN
     SELECT id, user_id INTO v_sponsor_affiliate_id, v_sponsor_user_id
     FROM public.affiliates
     WHERE organization_id = v_org_id
     ORDER BY created_at ASC
     LIMIT 1;
   END IF;

   -- 5. Inserir na tabela user_profiles
   INSERT INTO public.user_profiles (
     id, email, role, full_name, login, 
     organization_id, sponsor_id, referrer_id,
     status, rank, created_at, updated_at
   )
   VALUES (
     new.id, 
     new.email, 
     COALESCE(new.raw_user_meta_data ->> 'role', 'affiliate'),
     v_full_name,
     v_login,
     v_org_id,
     v_sponsor_user_id,
     v_sponsor_user_id,
     'active',
     'Consultor',
     new.created_at, 
     new.created_at
   )
   ON CONFLICT (id) DO UPDATE SET
     full_name = EXCLUDED.full_name,
     login = EXCLUDED.login,
     organization_id = EXCLUDED.organization_id,
     sponsor_id = EXCLUDED.sponsor_id;

   -- 6. Inserir na tabela affiliates
   INSERT INTO public.affiliates (
     user_id, email, full_name, referral_code, 
     organization_id, sponsor_id, 
     is_active, created_at, updated_at
   )
   VALUES (
     new.id,
     new.email,
     v_full_name,
     v_login,
     v_org_id,
     v_sponsor_affiliate_id,
     false, -- Começa inativo até comprar a primeira cesta básica
     new.created_at,
     new.created_at
   )
   ON CONFLICT (user_id) DO UPDATE SET
     full_name = EXCLUDED.full_name,
     referral_code = EXCLUDED.referral_code,
     sponsor_id = EXCLUDED.sponsor_id;

   -- 7. Inserir na tabela user_settings
   INSERT INTO public.user_settings (user_id, organization_id, created_at, updated_at)
   VALUES (new.id, v_org_id, new.created_at, new.created_at)
   ON CONFLICT (user_id) DO NOTHING;
   
   RETURN new;
 END;
 $function$;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_affiliate_user();


-- E. Trigger: Ativação Automática de 30 Dias (CELD Activation)
CREATE OR REPLACE FUNCTION public.handle_celd_activation()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_is_celd_plan boolean := false;
    v_current_active_until timestamp with time zone;
    v_new_active_until timestamp with time zone;
BEGIN
    -- Só processa se o status mudar para 'Pago'
    IF (OLD.status <> 'Pago' AND NEW.status = 'Pago') THEN
        
        -- Verifica se o pedido contém o item de Plano Único (R$ 300,00 ou nome com Cesta)
        SELECT EXISTS (
            SELECT 1 FROM public.order_items oi
            JOIN public.products p ON oi.product_id = p.id
            LEFT JOIN public.product_categories pc ON p.category_id = pc.id
            WHERE oi.order_id = NEW.id
            AND (oi.unit_price = 300.00 OR p.price = 300.00 OR p.name ILIKE '%Cesta%' OR pc.name ILIKE '%Cesta%')
        ) INTO v_is_celd_plan;

        -- Se for a ativação da Cesta Básica CELD e o usuário estiver definido
        IF v_is_celd_plan AND NEW.user_id IS NOT NULL THEN
            -- Pega data de vencimento atual
            SELECT active_until INTO v_current_active_until 
            FROM public.affiliates 
            WHERE user_id = NEW.user_id 
            AND organization_id = NEW.organization_id
            LIMIT 1;

            -- Se o usuário já estiver ativo (data no futuro), adiciona mais 30 dias.
            -- Se estiver vencido ou nulo, inicia os 30 dias a partir da data de hoje.
            IF v_current_active_until IS NOT NULL AND v_current_active_until >= NOW() THEN
                v_new_active_until := v_current_active_until + INTERVAL '30 days';
            ELSE
                v_new_active_until := NOW() + INTERVAL '30 days';
            END IF;

            -- Grava o novo prazo em affiliates
            UPDATE public.affiliates 
            SET 
                active_until = v_new_active_until,
                is_active = true,
                updated_at = NOW()
            WHERE user_id = NEW.user_id 
            AND organization_id = NEW.organization_id;

            -- Grava o novo prazo em user_settings
            UPDATE public.user_settings 
            SET 
                active_until = v_new_active_until,
                is_active_this_month = true,
                updated_at = NOW()
            WHERE user_id = NEW.user_id 
            AND organization_id = NEW.organization_id;

            -- Grava log de auditoria
            INSERT INTO public.debug_logs (operation, message, metadata)
            VALUES (
                'CELD_ACTIVATION',
                'Afiliado ' || NEW.user_id || ' ativado até ' || v_new_active_until,
                jsonb_build_object('order_id', NEW.id, 'user_id', NEW.user_id, 'previous_active_until', v_current_active_until, 'new_active_until', v_new_active_until)
            );
            
        END IF;
    END IF;

    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trigger_handle_celd_activation ON public.orders;
CREATE TRIGGER trigger_handle_celd_activation
    AFTER UPDATE ON public.orders
    FOR EACH ROW
    WHEN (OLD.status <> 'Pago' AND NEW.status = 'Pago')
    EXECUTE FUNCTION public.handle_celd_activation();


-- F. Trigger: Distribuição de Comissões MMN com Compressão Dinâmica (Verifica active_until)
CREATE OR REPLACE FUNCTION public.distribute_commissions()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_affiliate RECORD;
    v_current_sponsor_id uuid;
    v_sponsor_rec RECORD;
    v_config RECORD;
    v_commission_amount numeric;
    v_gen_count integer := 0;
    v_active_gens integer;
    v_target_user_id uuid;
BEGIN
    -- Só processa se o status mudar para 'Pago'
    IF (OLD.status IS NULL OR OLD.status != 'Pago') AND NEW.status = 'Pago' THEN
        
        -- Evita pagamento em duplicidade
        IF EXISTS (SELECT 1 FROM public.commissions WHERE order_id = NEW.id) THEN
            RETURN NEW;
        END IF;

        -- Busca Configuração Unilevel Geral da Organização
        SELECT * INTO v_config 
        FROM public.commission_configs 
        WHERE key = 'geral' 
        AND organization_id = NEW.organization_id;
        
        IF v_config IS NULL THEN
            RETURN NEW;
        END IF;

        v_active_gens := v_config.active_generations;

        -- 1. Identificar o Afiliado que indicou a venda (Nível de indicação inicial)
        -- Prioridade 1: Código de indicação no pedido
        IF NEW.referral_code IS NOT NULL AND NEW.referral_code != '' THEN
            SELECT * INTO v_affiliate FROM public.affiliates 
            WHERE LOWER(referral_code) = LOWER(NEW.referral_code) 
            AND organization_id = NEW.organization_id
            LIMIT 1;
        END IF;

        -- Prioridade 2: Padrinho direto cadastrado no perfil do comprador
        IF v_affiliate IS NULL AND NEW.user_id IS NOT NULL THEN
            SELECT a.* INTO v_affiliate 
            FROM public.affiliates a
            JOIN public.user_profiles p ON p.sponsor_id = a.user_id
            WHERE p.id = NEW.user_id 
            AND a.organization_id = NEW.organization_id
            LIMIT 1;
        END IF;

        -- Se não houver padrinho ou afiliado, a comissão não sobe
        IF v_affiliate IS NULL THEN
            RETURN NEW;
        END IF;

        -- Inicia subida na rede a partir do patrocinador direto
        v_current_sponsor_id := v_affiliate.id;

        -- 2. Loop de Distribuição das Gerações (Unilevel 10 Níveis com Compressão Dinâmica)
        WHILE v_gen_count < v_active_gens AND v_current_sponsor_id IS NOT NULL LOOP
            -- Pega informações do patrocinador da vez
            SELECT user_id, sponsor_id, active_until, is_active 
            INTO v_sponsor_rec 
            FROM public.affiliates 
            WHERE id = v_current_sponsor_id 
            AND organization_id = NEW.organization_id;
            
            IF v_sponsor_rec IS NULL THEN
                EXIT;
            END IF;

            -- COMPRESSÃO DINÂMICA CELD:
            -- Só paga a comissão da geração atual se o patrocinador estiver com mensalidade ativa (active_until >= hoje)
            IF v_sponsor_rec.active_until IS NOT NULL AND v_sponsor_rec.active_until >= NOW() THEN
                -- Incrementa o nível de pagamento a ser distribuído
                v_gen_count := v_gen_count + 1;
                
                -- Carrega percentual do nível
                SELECT (lvl->>'value')::numeric INTO v_commission_amount
                FROM jsonb_array_elements(v_config.levels) AS lvl
                WHERE (lvl->>'level')::integer = v_gen_count;

                IF v_commission_amount IS NOT NULL AND v_commission_amount > 0 THEN
                    -- Se a comissão for porcentagem, calcula em cima do total da compra
                    IF v_config.type = 'percent' THEN
                        v_commission_amount := NEW.total_amount * (v_commission_amount / 100);
                    END IF;

                    -- Credita o valor na carteira do patrocinador
                    UPDATE public.user_settings 
                    SET 
                        total_earnings = total_earnings + v_commission_amount,
                        available_balance = available_balance + v_commission_amount,
                        updated_at = now()
                    WHERE user_id = v_sponsor_rec.user_id;

                    -- Insere registro histórico na tabela de comissões
                    INSERT INTO public.commissions (
                        organization_id,
                        user_id,
                        order_id,
                        amount,
                        level,
                        commission_type,
                        description
                    ) VALUES (
                        NEW.organization_id,
                        v_sponsor_rec.user_id,
                        NEW.id,
                        v_commission_amount,
                        v_gen_count,
                        v_config.type,
                        'Comissão Unilevel G' || v_gen_count || ' - Indicação no Pedido ' || NEW.id
                    );
                END IF;
            ELSE
                -- Patrocinador está INATIVO/INADIMPLENTE!
                -- A comissão de Geração não é creditada a ele.
                -- Graças à compressão dinâmica, pulamos ele e continuamos a busca na linha ascendente
                -- para pagar esta geração a um padrinho ativo acima, garantindo o repasse correto do plano.
                INSERT INTO public.debug_logs (operation, message, metadata)
                VALUES (
                    'COMMISSION_COMPRESSION_SKIP',
                    'Sponsor ' || v_sponsor_rec.user_id || ' inativo. Comissão Geração pulada/comprimida.',
                    jsonb_build_object('order_id', NEW.id, 'inactive_user_id', v_sponsor_rec.user_id, 'active_until', v_sponsor_rec.active_until)
                );
            END IF;

            -- Sobe para o patrocinador do patrocinador atual
            v_current_sponsor_id := v_sponsor_rec.sponsor_id;
            
            -- Salvaguarda contra loops infinitos
            IF v_gen_count > 50 THEN EXIT; END IF;
        END LOOP;
    END IF;

    RETURN NEW;
END;
$$;

